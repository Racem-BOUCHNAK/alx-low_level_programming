#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
*op_add- entry point of the code
*@a:var
*@b:var
* Return: result
**/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub- entry point of the code
*@a:var
*@b:var
* Return: result
**/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul- entry point of the code
*@a:var
*@b:var
* Return: result
**/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div- entry point of the code
*@a:var
*@b:var
* Return: result
**/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod- entry point of the code
*@a:var
*@b:var
* Return: result
**/
int op_mod(int a, int b)
{
return (a % b);
}
